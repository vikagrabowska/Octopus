function changeTextSize_1() {
    for (i = 0; i < 1; i++) {
        var input = document.getElementById('inp-f-size-1').value;
        const n = document.querySelectorAll('#p-t1')
        n.forEach(el => {
            el.style.fontSize = input;
        });
    }
}
